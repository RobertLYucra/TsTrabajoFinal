package Beans;

/**
 *
 * @author RobertLY
 */
public class Alumno {
    private String cod;
    private String name; 
    private String email;
    private String password;

    public Alumno() {
    }

    public Alumno(String cod, String name, String email, String password) {
        this.cod = cod;
        this.name = name;
        this.email = email;
        this.password = password;
    }

    public String getCod() {
        return cod;
    }

    public void setCod(String cod) {
        this.cod = cod;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
    
}
